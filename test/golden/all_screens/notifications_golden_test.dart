// Golden tests for Notifications feature screens
import 'package:flutter/material.dart';
import 'package:flutter_test/flutter_test.dart';
import 'package:usdc_wallet/features/notifications/views/notification_permission_screen.dart';
import 'package:usdc_wallet/features/notifications/views/notification_preferences_screen.dart';
import 'package:usdc_wallet/features/notifications/views/notifications_view.dart';

import '../helpers/golden_test_helper.dart';

void main() {
  setUpAll(() async {
    await GoldenTestUtils.init();
  });

  group('NotificationsView Golden Tests', () {
    testWidgets('light mode', (tester) async {
      await tester.binding.setSurfaceSize(GoldenTestConfig.defaultSize);
      await tester.pumpWidget(
        GoldenTestWrapper(
          isDarkMode: false,
          child: NotificationsView(),
        ),
      );
      await tester.pump(const Duration(milliseconds: 100));

      await expectLater(
        find.byType(MaterialApp),
        matchesGoldenFile('goldens/notifications/list/default_light.png'),
      );
    });

    testWidgets('dark mode', (tester) async {
